package com.billy.magictower.model;

public class ShopCount {
    private int atkCount,defCount,hpCount;

    public int getAtkCount() {
        return atkCount;
    }

    public void setAtkCount(int atkCount) {
        this.atkCount = atkCount;
    }

    public int getDefCount() {
        return defCount;
    }

    public void setDefCount(int defCount) {
        this.defCount = defCount;
    }

    public int getHpCount() {
        return hpCount;
    }

    public void setHpCount(int hpCount) {
        this.hpCount = hpCount;
    }
}
